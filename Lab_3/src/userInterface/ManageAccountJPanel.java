    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAccount.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row to view", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Account account = (Account)tblAccount.getValueAt(selectedRow, 0);
            ViewAccountJPanel panel = new ViewAccountJPanel(userProcessContainer, account); //as we need only one account to be passed to next page
            userProcessContainer.add("ViewAccountJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);   
            
            
        }
        
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        //validation to check if fields entered
         if(!checkValidate())
            JOptionPane.showMessageDialog(this, "Please enter Account Number to search!");
         else{
       Account result = accountDirectory.searchAccount(txtAccountNumber.getText());
       if(result == null){
            JOptionPane.showMessageDialog(null,"Account Number entered does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
       } else
       {
           ViewAccountJPanel panel = new ViewAccountJPanel(userProcessContainer, result); //as we need only one account to be passed to next page
            userProcessContainer.add("ViewAccountJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);  
       }
         }
    }//GEN-LAST:event_btnSearchActionPerformed

