    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblAccount.getModel(); //we populate table
        dtm.setRowCount(0);
        //Add account from AccountDirectory list to the table
        for (Account account : accountDirectory.getAccountList()){ //this gives list of account from directory
        //new row created with 4 columns
        Object[] row = new Object[4];
        row[0] = account;
        row[1] = account.getRoutingNumber();
        row[2] = account.getAccountNumber();
        row[3] = account.getBalance();
        
        dtm.addRow(row); //added row
          
        }
    }
    
        public boolean checkValidate(){
        return Validations.isStringValid(txtAccountNumber.getText()) && txtAccountNumber.getText().matches("^[0-9]+$");
    }
       
